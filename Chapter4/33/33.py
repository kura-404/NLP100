# spaCyライブラリをインポートする（自然言語処理の基本ライブラリ）
import spacy

# GiNZAの日本語モデル（ELECTRAベース）を読み込む
nlp = spacy.load("ja_ginza_electra")

# 係り受け解析を行いたい日本語のテキストを定義する
text = """
メロスは激怒した。
必ず、かの邪智暴虐の王を除かなければならぬと決意した。
メロスには政治がわからぬ。
メロスは、村の牧人である。
笛を吹き、羊と遊んで暮して来た。
けれども邪悪に対しては、人一倍に敏感であった。
"""

# テキストを解析して、形態素と係り受け情報を含むオブジェクトに変換する
doc = nlp(text)

# 各トークン（単語）についてループ処理を行う
for token in doc:
    # 各トークンの表層形と、係っている先の語（係り先）をタブ区切りで出力
    print(f"{token.text}\t{token.head.text}")